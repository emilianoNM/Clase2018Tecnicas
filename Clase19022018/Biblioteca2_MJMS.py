class Biblioteca():
      Titulo=None
      Edicion=None
      Paginas=None
      Disponible=None   

class Mate(Biblioteca):
      Autor=None
      Seccion=None
      Capitulos=None 
      
      
      def __init__(self,Titulo,Edicion,Paginas,Disponible,Autor,Seccion,Capitulos):
            self.Titulo=Titulo
            self.Edicion=Edicion
            self.Paginas=Paginas
            self.Disponible=Disponible
            self.Autor=Autor
            self.Seccion=Seccion
            self.Capitulos=Capitulos
      
      def __str__(self):
         return self.Titulo
 
      def Datos(self):
         print('\n'"Datos del libro 1:")
         print("Título:",str(self.Titulo),'\n'"Edición:",str(self.Edicion),'\n'"Número de páginas:",str(self.Paginas),'\n'"Disponible para prestamo:",str(self.Disponible)) 
      
      def Tulibro(self) :
         print('\n'"El libro ",str(self),"del autor",str(self.Autor),", pertenece a la sección de", str(self.
Seccion),"y tiene",str(self.Capitulos),"capítulos")

