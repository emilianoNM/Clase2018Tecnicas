class Gorila:
      def __init__(self,nombre,color,crc):
         self.nombre=nombre
         self.color=color
         self.crc=crc
         self.mastrucos=[]
         self.comer=[]
         self.bostezar=[]   
		 
      def Truco1(self,ladrido,vuelo,carrera,nado):
          self.mastrucos.append(ladrido) 
          self.mastrucos.append(vuelo)
          self.mastrucos.append(carrera)
          self.mastrucos.append(nado)          
      def Truco2(self,comida):
          self.comer.append(comida)             
      def Truco3(self,bostezo):
          self.bostezar.append(bostezo)
