class Especie():
      Nombre=None
      Alimentacion=None
      Edad=None

class Rinoceronte(Especie):
      Tamano=None
      Pasatiempo=None
      Amigable=None 
      
      def __init__(self,Nombre,Alimentacion,Edad, Tamano, Pasatiempo, Amigable):
            self.Nombre=Nombre
            self.Alimentacion=Alimentacion
            self.Edad=Edad
            self.Tamano=Tamano
            self.Pasatiempo=Pasatiempo
            self.Amigable=Amigable
      
      def __str__(self):
         return self.Nombre
 
      def Datos(self):
         print("Datos del animal 1:")
         print("Nombre:",str(self.Nombre),'\n'"Alimentacion:",str(self.Alimentacion),'\n'"Edad:",str(self.Edad)) 
      
      def Caracter(self) :
         print('\n'"El rinoceronte",str(self),"es",str(self.Tamano),", Su pasatiempo favorito es", str(self.Pasatiempo), "y",str(self.Amigable))


