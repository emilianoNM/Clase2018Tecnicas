class Lavanderia():
      Prenda=None
      Suciedad=None
      Color=None
      

class Prenda1(Lavanderia):
      Detergente=None
      Servicio=None
      Tipotejido=None 
      
      def __init__(self,Prenda,Suciedad,Color,Detergente,Servicio,Tipotejido):
            self.Prenda=Prenda
            self.Suciedad=Suciedad
            self.Color=Color
            self.Detergente=Detergente
            self.Servicio=Servicio
            self.Tipotejido=Tipotejido
            
      
      def __str__(self):
         return self.Prenda
 
      def Info(self):
         print('\n'"Características de tu prenda 1:")
         print("Prenda:",str(self.Prenda),'\n'"Nivel de suciedad:",str(self.Suciedad),'\n'"Color:",str(self.Color)) 
        
        
      def Lavamos(self) :
         print('\n'"Tus", str(self),"se lavaran con",str(self.Detergente),"debido a que son de",str(self.Tipotejido))
         print('\n'"Elegiste los siguientes servicios:",str(self.Servicio))

  

